#alfabetical order of the list
cars=['bmw','audi','toyota','subaru']
cars.sort()
print(cars)

#reverse-alphabetical order
cars=['bmw','audi','toyota','subaru']
cars.sort(reverse=True)
print(cars)

#Sorting a List Temporarily with the sorted() Function

cars=['bmw','audi','toyota','subaru']
print(sorted(cars))
print(cars)

#Printing a List in Reverse Order

cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)

cars.reverse()
print(cars)

